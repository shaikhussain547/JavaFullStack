package BasicJava;

public class Introduction {
		
		// A set of such instructions is known as a program and the act of creating a program is known as programming.
		// Programs are created to solve the problems, to automate the processes and to reduce repetitive and/or manual work.
		
		/* The first step to write a program is to create an algorithm. 
		    An algorithm is a process or set of rules to be followed by the computer while solving a problem. 
		    Algorithm should be represented into a form which others can easily understand.
		    There are primarily two ways of representing an algorithm:
					Pseudo-code: represents the algorithm in a way that is in between a programming language and English statements
					Flowchart: represents the algorithm in a diagrammatic way.		*/
		
		/* Flowchart : Flowchart is a diagrammatic representation of sequence of logical steps of a program.
		 *	Symbols : Oval --> Start/Stop
		 *					parallelogram --> Input/Output
		 *					Rectangle --> Process
		 *					Diamond  --> Decision
		 *					Down arrow --> Connecting two componenets
		 *					Circle --> On-page Connector
		 *					Pentagon --> Off-page Connector
		 */
		
		/* Algorithm : Algorithm is a step-by-step procedure, which defines a set of instructions to be executed 
		 * 					  in a certain order to get the desired output.
		 * 				-->  The term "Efficiency" refers to the ability of an algorithm to solve the problem with minium time 
		 * 					  and resources. An efficient performs the task quickly and with optimal use of computational resources.
		 * 				-->  The algorithm that involves breaking a problem into smaller subproblems and solving each subproblem  
		 * 					  individually is called "Recursion".
		 * 				-->  In recursive algorithm, the same function is called with smaller inputs until a base case is reached.
		 * 				-->  An algorithm must always terminate after a finite number of steps. It should not result in an infinite loop.
		 * 				-->  The process of testing an algorithm with different inputs to verify its correctness is called Testing.
		 * 				-->  An algorithm can have multiple solutions to a given problem. 
		 */
		
		/* Pseudocode : A way of expressing an algorithm without confirming to specific syntax rules.
		 * 					--> An informal high-level representation of the actual code.
		 *  Example :-  set i to 0
		 *  					for each i from 0 to 9
		 *  						if i is odd
		 *  								print i
		 *  						end for loop
		 */
		
		/* Time Complexity : The time complexity of an algorithm quantifies the amount of time taken by an algorithm 
		 * 								  to run as a function of the length of the input.
		 *  Space Complexity : Problem-solving using computer requires memory to hold temporary data or final result
		 *  							   while the program is in execution. The amount of memory required by the algorithm to solve given problem.
		 */
	
	/*  We write programs in programming languages like Java which are called as High-level programming languages.
	 *   But computers cannot understand high-level languages, they understand only binary language, i.e., 0's and 1's.
	 *   This is the reason that every program that we write needs to get converted into binary form for the computer to understand.
	 *   This conversion is made possible using system software called compilers and interpreters.
	 *   
	 *   In Java, the program (source code) written by the programmer gets compiled and converted into byte code (compiled code) by the Java compiler.
	 *   
	 *   All the byte codes required for the program are then given to the interpreter.
	 *   The interpreter reads the byte code line by line, converts it to binary form, also called as machine language or binary language, and then executes it.
	 *   
	 *   This process requires some support and an environment for the execution to happen.
	 *   JDK (Java Development Kit) helps in this for execution of a Java program.
	 *   It provides library support and Java Runtime Environment (JRE), the environment for developing and executing Java based applications and programs.
	 */
	
	/*		Java features :
	 *	 	1. Simple : High-level, Easy to learn.
	 * 		2. Object Oriented
	 * 		3. Robust : Strong type checking mechanism and Automatic Memory Management.
	 * 		4. Architecture Neutral : Program written on a platform can be executed on other platforms without recompilation.
	 * 		5. Secure : Execution inside contained environment, Cannot access data using memory address.
	 * 		6. Multi-threaded : A program can have multiple threads executing independently and simultaneously.
	 */
	
	
	
	
	
		
}
